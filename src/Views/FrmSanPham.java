    private void loadDataTable() {
        lstQLSP = _iManageSanPhamService.getAll();
        dtm = (DefaultTableModel) tblSanPham.getModel();
        dtm.setRowCount(0);
        for (QLSP x : lstQLSP) {
            dtm.addRow(new Object[]{lstQLSP.indexOf(x) + 1, x.getMa(), x.getTen()});
        }
    }

    private QLSP getSPFromInput() {
        return new QLSP(txtMa.getText(), txtTen.getText());
    }

    private String getID() {
        int index = tblSanPham.getSelectedRow();
        return lstQLSP.get(index).getId();
    }


    private void loadDataToTable() {
        dtm = (DefaultTableModel) tblChiTietSP.getModel();
        lstQLChiTietSP = _iManageChiTietSPService.getAll();
        dtm.setRowCount(0);
        int count = 1;
        for (QLCtsp x : lstQLChiTietSP) {
            dtm.addRow(new Object[]{count, x.getTensp(), x.getTennsx(), x.getTenms(), x.getTendsp(), x.getNambh(), x.getMota(), x.getSoluongton(), x.getGianhap(), x.getGiaban()});
            count++;
        }
    }

    private QLCtsp getData() {
        int isp = cboTenSP.getSelectedIndex();
        int insx = cboNsx.getSelectedIndex();
        int ims = cboTenMS.getSelectedIndex();
        int idsp = cboTenDSP.getSelectedIndex();

        QLSP qlsp = lstQLSP.get(isp);
        QLNSX qlnsx = lstQLNSX.get(insx);
        QLMauSac qlms = lstQLMS.get(ims);
        QLDongSP qldsp = lstQLDSP.get(idsp);
        int nambh = Integer.parseInt(txtNamBH.getText());
        String mota = txtMoTa.getText();
        int sl = Integer.parseInt(txtSoLuong.getText());
        float gn = Float.parseFloat(txtGiaNhap.getText());
        float gb = Float.parseFloat(txtGiaBan.getText());

        return new QLCtsp(qlsp.getTen(), qlnsx.getTen(), qlms.getTen(), qldsp.getTen(), nambh, mota, sl, gn, gb);

    }

    private String getID() {
        int index = tblChiTietSP.getSelectedRow();
        return lstQLChiTietSP.get(index).getId();
    }

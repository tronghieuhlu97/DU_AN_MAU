    private void tblChiTietSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblChiTietSPMouseClicked
        row = tblChiTietSP.getSelectedRow();
        if (row == -1) {
            return;
        }
        dcbosp.setSelectedItem(tblChiTietSP.getValueAt(row, 1));
        dcbonsx.setSelectedItem(tblChiTietSP.getValueAt(row, 2));
        dcboms.setSelectedItem(tblChiTietSP.getValueAt(row, 3));
        dcbodsp.setSelectedItem(tblChiTietSP.getValueAt(row, 4));
        txtNamBH.setText(tblChiTietSP.getValueAt(row, 5).toString());
        txtMoTa.setText(tblChiTietSP.getValueAt(row, 6).toString());
        txtSoLuong.setText(tblChiTietSP.getValueAt(row, 7).toString());
        txtGiaNhap.setText(tblChiTietSP.getValueAt(row, 8).toString());
        txtGiaBan.setText(tblChiTietSP.getValueAt(row, 9).toString());
    }//GEN-LAST:event_tblChiTietSPMouseClicked


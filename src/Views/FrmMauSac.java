    private void loadDataToTable() {
        lstQLMauSac = _iManageMauSacService.getAll();
        dtm = (DefaultTableModel) tblMauSac.getModel();
        dtm.setRowCount(0);
        for (QLMauSac x : lstQLMauSac) {
            dtm.addRow(new Object[]{lstQLMauSac.indexOf(x) + 1, x.getMa(), x.getTen()});
        }
    }

    private QLMauSac getMauSacFromInput() {
        return new QLMauSac(txtMa.getText(), txtTen.getText());
    }

    private String getID() {
        int index = tblMauSac.getSelectedRow();
        return lstQLMauSac.get(index).getId();
    }

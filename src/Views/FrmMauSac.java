    private void tblMauSacMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMauSacMouseClicked
        row = tblMauSac.getSelectedRow();
        if (row == -1) {
            return;
        }

        String ma = tblMauSac.getValueAt(row, 1).toString();
        String ten = tblMauSac.getValueAt(row, 2).toString();

        txtMa.setText(ma);
        txtTen.setText(ten);
    }//GEN-LAST:event_tblMauSacMouseClicked

    private void btnDELETEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDELETEActionPerformed
        int hoi = JOptionPane.showConfirmDialog(this, "Ban có muốn xóa không?", "Xóa", JOptionPane.YES_NO_OPTION);
        if (hoi == JOptionPane.YES_OPTION) {
            JOptionPane.showMessageDialog(this, _iManageMauSacService.delete(getID()));
            loadDataToTable();
        }
    }//GEN-LAST:event_btnDELETEActionPerformed

    private void btnADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnADDActionPerformed
        int hoi = JOptionPane.showConfirmDialog(this, "Ban có muốn thêm không?", "Thêm", JOptionPane.YES_NO_OPTION);
        if (hoi == JOptionPane.YES_OPTION) {
            for (int i = 0; i < lstQLMauSac.size(); i++) {
                if (lstQLMauSac.get(i).getMa().equalsIgnoreCase(txtMa.getText())) {
                    JOptionPane.showMessageDialog(this, "Mã không được trùng");
                    return;
                }
            }
            JOptionPane.showMessageDialog(this, _iManageMauSacService.add(getMauSacFromInput()));
            for (QLMauSac qLMauSac : lstQLMauSac) {
                System.out.println(qLMauSac.toString());
            }
            loadDataToTable();
        }


    }//GEN-LAST:event_btnADDActionPerformed

    private void btnEDITActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEDITActionPerformed
        int hoi = JOptionPane.showConfirmDialog(this, "Ban có muốn sửa không?", "Sửa", JOptionPane.YES_NO_OPTION);
        if (hoi == JOptionPane.YES_OPTION) {
            for (int i = 0; i < lstQLMauSac.size(); i++) {
                if (row != i && lstQLMauSac.get(i).getMa().equalsIgnoreCase(txtMa.getText())) {
                    JOptionPane.showMessageDialog(this, "Mã đã tồn tại");
                    return;
                }
            }
            try {
                JOptionPane.showMessageDialog(this, _iManageMauSacService.update(getID(), getMauSacFromInput()));
                loadDataToTable();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Mời chọn lại");
            }

        }
    }//GEN-LAST:event_btnEDITActionPerformed


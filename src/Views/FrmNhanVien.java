    private void loadDataToTable() {
        dtm = (DefaultTableModel) tblNV.getModel();
        dtm.setRowCount(0);
        lstQLNV = _iManageNhanVienService.getAll();
        int count = 1;
        for (QLNv x : lstQLNV) {
            dtm.addRow(new Object[]{count, x.getMa(), x.getTen(), x.getTendem(), x.getHo(), x.getGioitinh(), x.getNgaysinh(), x.getDiachi(), x.getSdt(), x.getMatkhau(), _iManageCuaHangService.getById(x.getIdCH()).getTen(), _iManageChucVuService.getById(x.getIdCV()).getTen(), x.getTrangthai()});
            count++;
        }
    }


    private QLNv getData() throws ParseException {
        int ich = cboCuaHang.getSelectedIndex();
        int icv = cboChucVu.getSelectedIndex();
        
        DateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        Date date = format.parse(txtNS.getText());
        return new QLNv(txtMa.getText(), txtTen.getText(), txtTenDem.getText(), txtHo.getText(), txtGioiTinh.getText(), date, txtDC.getText(), txtSDT.getText(), txtMK.getText(), lstQLCH.get(ich).getId(), lstQLCV.get(icv).getId(), Integer.parseInt(txtTrangThai.getText()));
    }

    private String getID() {
        int index = tblNV.getSelectedRow();
        return lstQLNV.get(index).getId();
    }

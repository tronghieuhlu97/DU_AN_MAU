    private void btnADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnADDActionPerformed
        int hoi = JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm ko?", "Thêm", JOptionPane.YES_NO_OPTION);
        if (hoi == JOptionPane.YES_OPTION) {
            for (int i = 0; i < lstQLNV.size(); i++) {
                if (txtMa.getText().equalsIgnoreCase(lstQLNV.get(i).getMa())) {
                    JOptionPane.showMessageDialog(this, "Mã sản phẩm bị trùng");
                    return;
                }
            }

            try {
                JOptionPane.showMessageDialog(this, _iManageNhanVienService.add(getData()));
            } catch (ParseException ex) {
                Logger.getLogger(FrmNhanVien.class.getName()).log(Level.SEVERE, null, ex);
            }
            loadDataToTable();
        }


    }//GEN-LAST:event_btnADDActionPerformed

    private void btnEDITActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEDITActionPerformed
       int hoi = JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa không?","Sửa",JOptionPane.YES_NO_OPTION);
        if(hoi == JOptionPane.YES_OPTION){
            for(int i = 0;i<lstQLNV.size();i++){
                if(i!= row && lstQLNV.get(i).getMa().equalsIgnoreCase(txtMa.getText())){
                    JOptionPane.showMessageDialog(this, "Mã đã tồn tại");
                    return;
                }
            }
           try {
               JOptionPane.showMessageDialog(this, _iManageNhanVienService.edit(getID(), getData()));
           } catch (Exception ex) {
               JOptionPane.showMessageDialog(this, "Mời chọn lại");
           }
            loadDataToTable();
        }
    }//GEN-LAST:event_btnEDITActionPerformed

    private void btnDELETEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDELETEActionPerformed
        int hoi = JOptionPane.showConfirmDialog(this, "Ban có muốn xóa không?", "Xóa", JOptionPane.YES_NO_OPTION);
        if (hoi == JOptionPane.YES_OPTION) {
            try {
                JOptionPane.showMessageDialog(this, _iManageNhanVienService.delete(getID()));
                loadDataToTable();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Mời chọn lại");
            }

        }
    }//GEN-LAST:event_btnDELETEActionPerformed


    private void loadDataToTable() {
        dtm = (DefaultTableModel) tblCuaHang.getModel();
        lstQLCuaHang = _iManageCuaHangService.getAll();
        dtm.setRowCount(0);
        int count = 1;
        for (QLCuaHang x : lstQLCuaHang) {
            dtm.addRow(new Object[]{count, x.getMa(), x.getTen(), x.getDiachi(), x.getThanhpho(), x.getQuocgia()});
            count++;
        }
    }

    private QLCuaHang getData() {
        return new QLCuaHang(txtMa.getText(), txtTen.getText(), txtDiaChi.getText(), txtTP.getText(), txtQG.getText());
    }

    private String getID() {
        int index = tblCuaHang.getSelectedRow();
        return lstQLCuaHang.get(index).getId();
    }

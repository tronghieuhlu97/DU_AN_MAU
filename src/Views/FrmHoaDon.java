    private void btnADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnADDActionPerformed
        int hoi = JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm ko?", "Thêm", JOptionPane.YES_NO_OPTION);
        if (hoi == JOptionPane.YES_OPTION) {
            for (int i = 0; i < lstQLHD.size(); i++) {
                if (txtMa.getText().equalsIgnoreCase(lstQLHD.get(i).getMa())) {
                    JOptionPane.showMessageDialog(this, "Mã sản phẩm bị trùng");
                    return;
                }
            }

            try {
                JOptionPane.showMessageDialog(this, _iManageHoaDonService.add(getData()));
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn lại");
            }

            loadDataToTable();
        }
    }//GEN-LAST:event_btnADDActionPerformed

    private void btnEDITActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEDITActionPerformed
        int hoi = JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa?", "Sửa", JOptionPane.YES_NO_OPTION);
        if (hoi == JOptionPane.YES_OPTION) {
            for (int i = 0; i < lstQLHD.size(); i++) {
                if (row != i && lstQLHD.get(i).getMa().equalsIgnoreCase(txtMa.getText())) {
                    JOptionPane.showMessageDialog(this, "Mã đã tồn tại");
                    return;
                }
            }

            try {
                JOptionPane.showMessageDialog(this, _iManageHoaDonService.edit(getID(), getData()));
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Mời chọn lại");
            }

            loadDataToTable();
        }
    }//GEN-LAST:event_btnEDITActionPerformed

    private void btnDELETEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDELETEActionPerformed
        int hoi = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa?", "Xóa", JOptionPane.YES_NO_OPTION);
        if (hoi == JOptionPane.YES_OPTION) {
            try {
                JOptionPane.showMessageDialog(this, _iManageHoaDonService.delete(getID()));
                loadDataToTable();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Mời chọn lại");
            }

        }
    }//GEN-LAST:event_btnDELETEActionPerformed


    private QLhd getData() throws ParseException {

        DateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        Date dateNT = format.parse(txtNgayTao.getText());
        Date dateNTT = format.parse(txtNgayTT.getText());
        Date dateNS = format.parse(txtNgayShip.getText());
        Date dateNN = format.parse(txtNgayShip.getText());
        int ikh = cboKH.getSelectedIndex();
        int inv = cboNV.getSelectedIndex();
        return new QLhd(lstQLKH.get(ikh).getId(), lstQLNV.get(inv).getId(), txtMa.getText(), dateNT, dateNTT, dateNS, dateNN, Integer.parseInt(txtTT.getText()), txtTenNN.getText(), txtDC.getText(), txtSDT.getText());
    }

    private String getID() {
        int index = tblHD.getSelectedRow();
        return lstQLHD.get(index).getId();
    }

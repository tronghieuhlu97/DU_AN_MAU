    private void loadDataToTable() {
        dtm = (DefaultTableModel) tblHD.getModel();
        lstQLHD = _iManageHoaDonService.getAll();
        dtm.setRowCount(0);
        int count = 1;
        for (QLhd x : lstQLHD) {
            dtm.addRow(new Object[]{count, _iManageKhachHangService.getById(x.getIdkh()).getMa(), _iManageNhanVienService.getNVById(x.getIdnv()).getMa(), x.getMa(), x.getNgaytao(), x.getNgaythanhtoan(), x.getNgayship(), x.getNgaynhan(), x.getTinhtrang(), x.getTennguoinhan(), x.getDiachi(), x.getSdt()});
            count++;
        }

    }


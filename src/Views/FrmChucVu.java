    private void loadDataToTable() {
        lstqlChucVu = _iManageChucVuService.getAll();
        dtm = (DefaultTableModel) tblChucVu.getModel();
        dtm.setRowCount(0);
        int count = 1;
        for (QLChucVu x : lstqlChucVu) {
            dtm.addRow(new Object[]{count, x.getMa(), x.getTen()});
            count++;
        }
    }


    private QLChucVu getData() {
        return new QLChucVu( txtMa.getText(), txtTen.getText());
    }

    private String getID() {
        int index = tblChucVu.getSelectedRow();
        return lstqlChucVu.get(index).getId();
    }
